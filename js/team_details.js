/*
* Creates the dynamic html content based on team infromation
*/
function generateHTMLTeamDetails(teamName){
	var htmlCodeToInject = '';
	$('#team_title').html('<span>'+team_details[teamName]['team_name']+'</span>');
	var team_count = parseInt(team_details[teamName]['team_count']);
	var count_to_three = 1;
	for(var j=1;j<=team_count;j++){
		var member_name = team_details[teamName]['teamMember'+j]['name'];
		var member_image_location = team_details[teamName]['teamMember'+j]['picture_path'];
		var member_position = team_details[teamName]['teamMember'+j]['position'];
		if(count_to_three == 1)
			htmlCodeToInject += '<ul class="polaroids row" >';
		htmlCodeToInject += '<li class="col-sm-4" style="padding-top:40px;">'+
			'<a title="'+member_name+'">'+
				'<img src="'+member_image_location+' ">'+
				'<p style="font-size: 11px;"><i>'+member_position+'</i></p>'+
			'</a>'+
		'</li>';
		if(count_to_three == 3){
			htmlCodeToInject += '</ul>';
			count_to_three = 0;
		}
		count_to_three = count_to_three + 1;
	}
	$('#polaroid_canvas').html(htmlCodeToInject);
}

/*
* Parsing parameter from URL
* In this case need to read the URL for the team name and according display team
*/
$.urlParam = function(name){
    var results = new RegExp('[\?&]' + name + '=([^&#]*)').exec(window.location.href);
    if (results==null){
       return null;
    } else{
       return results[1] || 0;
    }
}
